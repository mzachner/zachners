package at.zachner.data.lebenslauf;

public class PersoenlicheDaten {

	
	
	private String titleVorher;
	private String titleNachher;
	private String vorname;
	private String nachname;
	private String geburtsdatum;
	private String familienstand;
	private Adresse adresse;
	private String telefonnummer;
	private String email;
	
	
	
	
	
	
	public String getTitleVorher() {
		return titleVorher;
	}
	public void setTitleVorher(String titleVorher) {
		this.titleVorher = titleVorher;
	}
	public String getTitleNachher() {
		return titleNachher;
	}
	public void setTitleNachher(String titleNachher) {
		this.titleNachher = titleNachher;
	}
	public String getVorname() {
		return vorname;
	}
	public void setVorname(String vorname) {
		this.vorname = vorname;
	}
	public String getNachname() {
		return nachname;
	}
	public void setNachname(String nachname) {
		this.nachname = nachname;
	}
	public String getGeburtsdatum() {
		return geburtsdatum;
	}
	public void setGeburtsdatum(String geburtsdatum) {
		this.geburtsdatum = geburtsdatum;
	}
	public String getFamilienstand() {
		return familienstand;
	}
	public void setFamilienstand(String familienstand) {
		this.familienstand = familienstand;
	}
	public Adresse getAdresse() {
		return adresse;
	}
	public void setAdresse(Adresse adresse) {
		this.adresse = adresse;
	}
	public String getTelefonnummer() {
		return telefonnummer;
	}
	public void setTelefonnummer(String telefonnummer) {
		this.telefonnummer = telefonnummer;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
